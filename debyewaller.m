function DWF = debyewaller(theta, lambda, T)



temp = [280 290 293 295 300 310 320 330 340 350 375 400 450 500 600 ...
    700 800 900 1000];

b = [0.3106 0.3217 0.3250 0.3272 0.3328 0.3438 0.3549 0.3660 0.3771 ...
    0.381 0.4158 0.4435 0.4988 0.5540 0.6643 0.7744 0.8842 0.9936 1.1027];

blist = interp1(temp, b, T);

DWF = exp(-1*blist*(sind(theta)/lambda)^2);

end